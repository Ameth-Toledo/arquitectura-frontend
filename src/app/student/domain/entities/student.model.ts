export class Student {
    id: number;
    name: string;
    lastName: string;
    matricule: string;
    age: number;
  
    constructor(id: number, name: string, lastName: string, matricule: string, age: number) {
      this.id = id;
      this.name = name;
      this.lastName = lastName;
      this.matricule = matricule;
      this.age = age;
    }
  }
  